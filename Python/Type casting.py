x = 1  #int
y = 2.5 #float
z = "3" #str

y = int(y)
z = int(z)

print(type(x))
print(type(y))
print(type(z))
print(x+y+z)

x = float(x)
y = float(y)
z = float(z)

print(type(x))
print(type(y))
print(type(z))
print(x+y+z)

x = str(x)
y = str(y)
z = str(z)

print(type(x))
print(type(y))
print(type(z))
print(x+" "+y+" "+z)